    <h1>Son rôle?<br><br></h1>
    <p>C’est de “dessiner” ou plutôt de concevoir le graphisme d’un site ou d’un produit sur une page web Le webdesigner (appelé également infographiste ou graphiste) est, selon la formule consacrée, « le créatif multimédia du numérique ». C’est lui qui va traduire en style graphique le brief du client. Directeur artistique version Web, le webdesigner va concevoir et réaliser le design du site Web. Le webdesigner voit son périmètre d’intervention différer suivant la structure où il intervient. Dans une PME ou en indépendant, sa liberté sera d’autant plus grande qu’il condensera entre ses mains l’ensemble de la chaîne de conception graphique et artistique.
